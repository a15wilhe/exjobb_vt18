/* ---- AUT DB #1----- */
/*events - click, mouse, hover, tab, resize, scroll*//* add -- target */
CREATE TABLE IF NOT EXISTS click_table (
	logID INT AUTO_INCREMENT,	
    href TEXT,
	pageX INT,
	pageY INT,
	TIMESTAMP DATETIME DEFAULT CURRENT_TIMESTAMP,
	PRIMARY KEY (logID)
);

CREATE TABLE IF NOT EXISTS tabing_table (
	logID INT AUTO_INCREMENT,
	href TEXT,
    tabs INT,
	TIMESTAMP DATETIME DEFAULT CURRENT_TIMESTAMP,
	PRIMARY KEY (logID)
);/* count up total amount of tabs per page*/

CREATE TABLE IF NOT EXISTS browser_table (
	logID INT AUTO_INCREMENT,
	href TEXT,
	appCodeName TEXT,
	userAgent TEXT,
    ww INT,
	wh INT,
    sw INT,
	sh INT,
	TIMESTAMP DATETIME DEFAULT CURRENT_TIMESTAMP,
	PRIMARY KEY (logID)
);

CREATE TABLE IF NOT EXISTS resize_table (
	logID INT AUTO_INCREMENT,
	href TEXT,
    wwStart INT,
	whStart INT,
    wwNew INT,
	whNew INT,
    sw INT,
	sh INT,
	TIMESTAMP DATETIME DEFAULT CURRENT_TIMESTAMP,
	PRIMARY KEY (logID)
);

CREATE TABLE IF NOT EXISTS mousemove_table (
	logID INT AUTO_INCREMENT,
	href TEXT,
	mouseX INT,
	mouseY INT,
	TIMESTAMP DATETIME DEFAULT CURRENT_TIMESTAMP,
	PRIMARY KEY (logID)
);

CREATE TABLE IF NOT EXISTS hover_table (
	logID INT AUTO_INCREMENT,
	href TEXT,
	target TEXT,
	countUpSec INT,
	TIMESTAMP DATETIME DEFAULT CURRENT_TIMESTAMP,
	PRIMARY KEY (logID)
);

CREATE TABLE IF NOT EXISTS mousemoveComp_table (
	logID INT AUTO_INCREMENT,
	href TEXT,
	mouseX INT,
	mouseY INT,
    logSkips INT,
	TIMESTAMP DATETIME DEFAULT CURRENT_TIMESTAMP,
	PRIMARY KEY (logID)
);